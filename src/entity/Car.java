package entity;

public class Car {
	private int car_no;
	private Sort sort;
	private String car_model;
	private String car_millprice;
	private String car_shopprice;
	private String car_manufacturer;
	private String car_rank;
	private String car_bodystructure;
	private String car_lwh;
	private String car_engin;
	private String picture;
	public int getCar_no() {
		return car_no;
	}
	public void setCar_no(int car_no) {
		this.car_no = car_no;
	}
	public Sort getSort() {
		return this.sort;
	}
	public void setSort(Sort sort) {
		this.sort = sort;
	}
	public String getCar_model() {
		return car_model;
	}
	public void setCar_model(String car_model) {
		this.car_model = car_model;
	}
	public String getCar_millprice() {
		return car_millprice;
	}
	public void setCar_millprice(String car_millprice) {
		this.car_millprice = car_millprice;
	}
	public String getCar_shopprice() {
		return car_shopprice;
	}
	public void setCar_shopprice(String car_shopprice) {
		this.car_shopprice = car_shopprice;
	}
	public String getCar_manufacturer() {
		return car_manufacturer;
	}
	public void setCar_manufacturer(String car_manufacturer) {
		this.car_manufacturer = car_manufacturer;
	}
	public String getCar_rank() {
		return car_rank;
	}
	public void setCar_rank(String car_rank) {
		this.car_rank = car_rank;
	}
	public String getCar_bodystructure() {
		return car_bodystructure;
	}
	public void setCar_bodystructure(String car_bodystructure) {
		this.car_bodystructure = car_bodystructure;
	}
	public String getCar_lwh() {
		return car_lwh;
	}
	public void setCar_lwh(String car_lwh) {
		this.car_lwh = car_lwh;
	}
	public String getCar_engin() {
		return car_engin;
	}
	public void setCar_engin(String car_engin) {
		this.car_engin = car_engin;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
}
